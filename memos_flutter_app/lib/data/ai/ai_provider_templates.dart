AiProviderTemplate? findAiProviderTemplate(String templateId) {
  final normalized = templateId.trim();
  if (normalized.isEmpty) return null;
  for (final template in aiProviderTemplates) {
    if (template.templateId == normalized) return template;
  }
  return null;
}

String localizedAiProviderTemplateDisplayName(
  AiProviderTemplate template, {
  required bool isZh,
}) {
  if (!isZh) return template.displayName;
  return switch (template.templateId) {
    aiTemplateCustomOpenAi => '自定义 OpenAI 兼容',
    aiTemplateCustomAnthropic => '自定义 Anthropic',
    aiTemplateCustomGemini => '自定义 Gemini',
    _ => template.displayName,
  };
}

List<AiProviderTemplate> templatesForGroup(AiProviderTemplateGroup group) {
  return aiProviderTemplates
      .where((template) => template.group == group)
      .toList(growable: false);
}

List<AiBuiltinModelPreset> builtinModelPresetsForTemplate(
  AiProviderTemplate template,
) {
  return switch (template.templateId) {
    aiTemplateOpenAi => _openAiBuiltInModels,
    aiTemplateAnthropic => _anthropicBuiltInModels,
    aiTemplateGemini => _geminiBuiltInModels,
    aiTemplateDeepSeek => _deepSeekBuiltInModels,
    aiTemplateOpenRouter => _openRouterBuiltInModels,
    aiTemplateCustomOpenAi => _openAiBuiltInModels,
    aiTemplateCustomAnthropic => _anthropicBuiltInModels,
    aiTemplateCustomGemini => _geminiBuiltInModels,
    _ => const <AiBuiltinModelPreset>[],
  };
}

List<AiBuiltinModelPreset> builtinModelPresetsForTemplateId(String templateId) {
  final template = findAiProviderTemplate(templateId);
  if (template == null) return const <AiBuiltinModelPreset>[];
  return builtinModelPresetsForTemplate(template);
}

AiProviderTemplate templateForAdapter(AiProviderAdapterKind adapterKind) {
  final preferredTemplateId = switch (adapterKind) {
    AiProviderAdapterKind.openAiCompatible => aiTemplateOpenAi,
    AiProviderAdapterKind.anthropic => aiTemplateAnthropic,
    AiProviderAdapterKind.gemini => aiTemplateGemini,
    AiProviderAdapterKind.azureOpenAi => aiTemplateAzureOpenAi,
    AiProviderAdapterKind.ollama => aiTemplateOllama,
  };
  return findAiProviderTemplate(preferredTemplateId) ??
      aiProviderTemplates.first;
}

AiProviderTemplate matchTemplateForBaseUrl({
  required AiProviderAdapterKind adapterKind,
  required String baseUrl,
}) {
  final normalized = baseUrl.trim().toLowerCase();
  if (adapterKind == AiProviderAdapterKind.anthropic) {
    if (normalized.contains('anthropic')) {
      return findAiProviderTemplate(aiTemplateAnthropic) ??
          templateForAdapter(adapterKind);
    }
    return findAiProviderTemplate(aiTemplateCustomAnthropic) ??
        templateForAdapter(adapterKind);
  }
  if (adapterKind == AiProviderAdapterKind.gemini) {
    if (normalized.contains('googleapis')) {
      return findAiProviderTemplate(aiTemplateGemini) ??
          templateForAdapter(adapterKind);
    }
    return findAiProviderTemplate(aiTemplateCustomGemini) ??
        templateForAdapter(adapterKind);
  }
  if (adapterKind == AiProviderAdapterKind.azureOpenAi) {
    return findAiProviderTemplate(aiTemplateAzureOpenAi) ??
        templateForAdapter(adapterKind);
  }
  if (adapterKind == AiProviderAdapterKind.ollama) {
    return findAiProviderTemplate(aiTemplateOllama) ??
        templateForAdapter(adapterKind);
